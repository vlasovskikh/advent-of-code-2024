def test_small_1() -> None:
    lines = """
#######
#...#.#
#.....#
#..OO@#
#..O..#
#.....#
#######

<vv<<^^<<^^
""".strip().splitlines()
    warehouse1, moves = p15.parse_input(lines)
    warehouse2 = warehouse1.widen()
    warehouse2.run_moves(moves)
    assert str(warehouse2) == (
        textwrap.dedent(
            """
            ##############
            ##...[].##..##
            ##...@.[]...##
            ##....[]....##
            ##..........##
            ##..........##
            ##############
            """
        ).strip()
    )
