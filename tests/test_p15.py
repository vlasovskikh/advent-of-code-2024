import textwrap

from aoc24 import p15


def test_example_1() -> None:
    lines = """
########
#..O.O.#
##@.O..#
#...O..#
#.#.O..#
#...O..#
#......#
########

<^^>>>vv<v>>v<<
""".strip().splitlines()
    warehouse, moves = p15.parse_input(lines)
    assert warehouse.sum_boxes_coordinates_after_moves(moves) == 2028


